package org.openu.fimcmp.fin;

class PcNode {
    private int count;
    private int preOrder;

    PcNode() {
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getPreOrder() {
        return preOrder;
    }

    public void setPreOrder(int preOrder) {
        this.preOrder = preOrder;
    }
}
